//CHIEDO ALL'UTENTE DI INSERIRE I NUMERI E LI CONVERTO
var inputNumeroIntero1 = parseInt(prompt("Inserisci un numero intero: "));
var inputNumeroIntero2 = parseInt(prompt("Inserisci un altro numero intero: "));

//ADDIZIONE
var somma = inputNumeroIntero1 + inputNumeroIntero2;

console.log("La somma dei tuoi numeri è di: " , somma);

//SOTTRAZIONE
var differenza = inputNumeroIntero1 - inputNumeroIntero2;

console.log("La differenza dei tuoi numeri è di: " , differenza);

//DIVISIONE
var quoziente  = inputNumeroIntero1 / inputNumeroIntero2;

console.log("La quoziente dei tuoi numeri è di: " , quoziente );

//MOLTIPLICAZIONE
var prodotto = inputNumeroIntero1 * inputNumeroIntero2;

console.log("La prodotto dei tuoi numeri è di: " , prodotto);